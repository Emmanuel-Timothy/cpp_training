#include <iostream>

using namespace std;
//Variable Type
int main(){
    //Single Alphabet
    char grade = 'A';
    //Alphabet Text
    string school = "Olifant School";
    //Number
    int year = 2024;
    //Decimal(Use f to make it more ram efficient, without f, it will use double and then conv to float)
    float gpa = 3.9f;
    //Decimal(more capacity)
    double pi = 3.14159;
    //
    cout << grade << endl;
    cout << school << endl;
    cout << year << endl;
    cout << gpa << endl;
    cout << pi << endl;

}